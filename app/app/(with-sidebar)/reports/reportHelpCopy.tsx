export function reportHelpRegistrationsUnique(opts: {
  heroLeadToSale: string;
  factRegistrations: number;
  factUniqueRegistrants: number;
}): ReactNode {
  const showEventsLine = opts.factRegistrations > opts.factUniqueRegistrants;
  return (
    <>
      Сколько <strong>разных людей</strong> хотя бы раз зарегистрировались за выбранный период — каждый человек считается один
      раз. Пользователя мы сопоставляем так: если в данных есть постоянный ID из CRM, используем его; иначе — гостевой
      идентификатор браузера. Всего событий регистрации в данных может быть больше — здесь именно люди. Конверсия в плитке
      «Покупки» считается по <strong>событиям</strong>, а не по этому числу: {opts.heroLeadToSale}.
      {showEventsLine ? (
        <>
          <br />
          Всего событий регистрации за период: {opts.factRegistrations.toLocaleString("ru-RU")}.
        </>
      ) : null}
    </>
  );
}

